<script lang="ts">
	import { browser } from "$app/environment";
	import { api } from "$lib/api";
	/* import { goto } from "$app/navigation"; */

	// Состояние приложения
	let selectedTickers: string[] = [];
	let newsItems: any[] = [];
	let isLoading = true;
	let error: string | null = null;
	let showBalance = true;
	let feedMode: "all" | "recommendations" = "all";
	let isLoggedIn = false;
	let userBalance: { balance: number; currency: string } | null = null;
	let isBalanceLoading = false;
	let importantNews: any[] = [];
	let isImportantNewsLoading = true;
	let selectedTags: string[] = [];
	let isTradeModalOpen = false;
	let tradeDetails: {
		action: "buy" | "sell";
		quantity: number;
		displayTicker: string;
		tradeTicker: string;
		actionText: string;
	} | null = null;
	let isTradeLoading = false;

	$: availableTickers = Array.from(
		new Set(newsItems.map(item => item.ticker).filter((t): t is string => !!t))
	).sort();

	$: availableTags = Array.from(new Set(newsItems.flatMap(item => item.tags))).sort();

	// A new reactive variable for the displayed news, filtered from the master list.
	$: filteredNewsItems = newsItems.filter(item => {
		const tickerMatch =
			selectedTickers.length === 0 || (item.ticker && selectedTickers.includes(item.ticker));
		const tagMatch =
			selectedTags.length === 0 || item.tags.some((tag: string) => selectedTags.includes(tag));
		return tickerMatch && tagMatch;
	});

	// Показывать всегда 7 карточек (седьмая всегда заполнена)
	$: visibleStories = (() => {
		const stories = importantNews.slice(0, 7);
		while (stories.length < 7) {
			stories.push({
				id: -(stories.length + 1), // unique negative id for keys
				title: "Больше новостей за 24 часа нет",
				ticker: null,
				tags: []
			});
		}
		return stories;
	})();

	async function fetchAndApplyRecommendation(newsItem: any) {
		// Only fetch recommendation if a ticker exists
		if (!newsItem.ticker || newsItem.ticker.trim() === "") {
			const itemToUpdate = newsItems.find(item => item.id === newsItem.id);
			if (itemToUpdate) {
				itemToUpdate.recommendation.isLoading = false;
				itemToUpdate.recommendation.reasoning =
					"Рекомендации для новостей без тикера не предоставляются.";
				newsItems = [...newsItems];
			}
			return;
		}

		const token = browser ? localStorage.getItem("access_token") : null;
		if (!token) {
			// No need to log error here, main fetchNews will handle it.
			return;
		}

		console.log(`[Rec Fetch] Starting recommendation fetch for news ID: ${newsItem.id}`);
		try {
			const url = `http://176.124.212.149:8000/api/assistant/recommendations/news/${newsItem.id}`;
			console.log(`[Rec Fetch] Fetching from URL: ${url} using POST`);
			const recResponse = await fetch(url, {
				method: "POST",
				headers: {
					Authorization: `Bearer ${token}`
				}
			});

			console.log(`[Rec Fetch] Response status for news ID ${newsItem.id}: ${recResponse.status}`);

			if (!recResponse.ok) {
				const errorText = await recResponse.text();
				console.error(`[Rec Fetch] Bad response for news ${newsItem.id}:`, errorText);
				throw new Error(`Failed with status ${recResponse.status}`);
			}

			const recJson = await recResponse.json();
			const itemToUpdate = newsItems.find(item => item.id === newsItem.id);

			if (itemToUpdate) {
				let action = "hold";
				if (recJson.buy) action = "buy";
				else if (recJson.sell) action = "sell";

				itemToUpdate.recommendation = {
					isLoading: false,
					action: action,
					confidence: recJson.confidence,
					reasoning: recJson.reasoning,
					ticker: recJson.ticker,
					quantity: recJson.quantity
				};
				newsItems = [...newsItems]; // Trigger Svelte reactivity
			}
		} catch (recError) {
			console.error(`[Rec Fetch] CATCH block for news ${newsItem.id}. Error:`, recError);
			const itemToUpdate = newsItems.find(item => item.id === newsItem.id);
			if (itemToUpdate) {
				itemToUpdate.recommendation = {
					...itemToUpdate.recommendation,
					isLoading: false,
					reasoning: "Не удалось загрузить рекомендацию."
				};
				newsItems = [...newsItems]; // Trigger Svelte reactivity
			}
		}
	}

	async function fetchNews(mode: "recommendations" | "all") {
		if (!browser) return;

		const token = localStorage.getItem("access_token");

		if (!token) {
			error = "Для доступа к новостям необходимо авторизоваться.";
			isLoading = false;
			newsItems = [];
			isLoggedIn = false;
			console.error("[News Page] Auth token not found in localStorage. User needs to log in.");
			return;
		}

		console.log(`[News Page] Fetching news with mode: ${mode}`);
		isLoading = true;
		error = null;

		try {
			const params = new URLSearchParams({
				top: "20"
			});
			if (mode === "recommendations") {
				params.append("filter", "True");
			}

			const url = `http://176.124.212.149:8000/api/news/?${params.toString()}`;
			console.log(`[News Page] Fetching from URL: ${url}`);

			const response = await fetch(url, {
				headers: {
					Authorization: `Bearer ${token}`
				}
			});

			console.log(`[News Page] Response status: ${response.status}`);

			if (response.status === 401) {
				error = "Ваша сессия истекла. Пожалуйста, войдите снова.";
				isLoading = false;
				newsItems = [];
				isLoggedIn = false;
				// Очищаем невалидный токен
				localStorage.removeItem("access_token");
				console.error(
					"[News Page] Received 401 Unauthorized. Invalid token. User needs to log in."
				);
				return;
			}
			isLoggedIn = true;
			fetchBalance();
			fetchImportantNews();

			if (!response.ok) {
				const errorData = await response
					.json()
					.catch(() => ({ detail: "Ошибка загрузки новостей" }));
				console.error("[News Page] News fetch error data:", errorData);
				throw new Error(errorData.detail || "Ошибка загрузки новостей");
			}

			const data = await response.json();
			console.log(`[News Page] Successfully fetched ${data.length} news items.`);

			// 1. Map news and set a loading state for recommendations
			newsItems = data.map((item: any) => ({
				id: item.id,
				title: item.title,
				summary: item.summary,
				keyPoints: item.summary
					? item.summary.split(". ").filter((s: string) => s && s.length > 0)
					: [],
				ticker: item.tickers?.split(",")[0].trim() || null, // Set to null if no ticker
				sentiment: item.is_positive ? "positive" : "negative",
				source: "TBank News API",
				timestamp: item.created_at,
				recommendation: {
					isLoading: !!(item.tickers && item.tickers.trim() !== ""), // Only load if ticker exists
					action: "hold",
					confidence: 0,
					reasoning: "Загрузка рекомендации...",
					ticker: item.tickers?.split(",")[0].trim() || null,
					quantity: 0
				},
				tags: item.tags ? item.tags.split(",").map((t: string) => t.trim()) : [],
				percentageChange: parseFloat(
					(Math.random() * 2.5 * (item.is_positive ? 1 : -1)).toFixed(1)
				),
				user_interaction: null
			}));

			// 2. Asynchronously fetch recommendations for each item
			newsItems.forEach(fetchAndApplyRecommendation);
		} catch (e: any) {
			error = e.message || "Произошла ошибка при запросе новостей.";
			newsItems = [];
			console.error("[News Page] Error in fetchNews catch block:", e);
		} finally {
			isLoading = false;
		}
	}

	$: fetchNews(feedMode);

	function handleTickerSelect(ticker: string) {
		if (selectedTickers.includes(ticker)) {
			selectedTickers = selectedTickers.filter(t => t !== ticker);
		} else {
			selectedTickers = [...selectedTickers, ticker];
		}
	}

	function handleTagSelect(tag: string) {
		if (selectedTags.includes(tag)) {
			selectedTags = selectedTags.filter(t => t !== tag);
		} else {
			selectedTags = [...selectedTags, tag];
		}
	}

	async function performInteraction(
		newsId: number,
		action: "like" | "dislike",
		newInteractionState: "liked" | "disliked" | null
	) {
		if (!browser) return;
		const token = localStorage.getItem("access_token");
		if (!token) {
			alert("Для этого действия необходимо авторизоваться.");
			return;
		}

		const item = newsItems.find(i => i.id === newsId);
		if (!item) return;

		try {
			const response = await fetch(`http://176.124.212.149:8000/api/users/me/${action}`, {
				method: "POST",
				headers: { "Content-Type": "application/json", Authorization: `Bearer ${token}` },
				body: JSON.stringify({ news_id: newsId })
			});

			if (response.ok) {
				item.user_interaction = newInteractionState;
				newsItems = [...newsItems];
			} else {
				const errorData = await response.json();
				alert(`Ошибка: ${errorData.detail || "Не удалось выполнить действие"}`);
			}
		} catch (e) {
			alert("Ошибка сети при выполнении действия.");
		}
	}

	function handleLikeClick(newsId: number) {
		const item = newsItems.find(i => i.id === newsId);
		if (!item) return;

		if (item.user_interaction === "liked") {
			performInteraction(newsId, "dislike", null);
		} else {
			performInteraction(newsId, "like", "liked");
		}
	}

	function handleDislikeClick(newsId: number) {
		const item = newsItems.find(i => i.id === newsId);
		if (!item) return;

		if (item.user_interaction === "disliked") {
			performInteraction(newsId, "like", null);
		} else {
			performInteraction(newsId, "dislike", "disliked");
		}
	}

	async function initiateTrade(newsItem: any) {
		if (!browser) return;

		const { action, quantity, ticker: displayTicker } = newsItem.recommendation;
		if (action === "hold") return;

		isTradeLoading = true;

		const token = localStorage.getItem("access_token");
		if (!token) {
			alert("Ошибка аутентификации. Пожалуйста, войдите снова.");
			isTradeLoading = false;
			return;
		}

		try {
			// Fetch tradable shares to get the first one
			const tradableResponse = await fetch(
				"http://176.124.212.149:8000/api/invest/sandbox/tradable-shares",
				{ headers: { Authorization: `Bearer ${token}` } }
			);

			if (!tradableResponse.ok) {
				throw new Error("Не удалось получить список торгуемых акций.");
			}

			const tradableShares = await tradableResponse.json();
			if (tradableShares.length === 0) {
				alert("Нет доступных акций для торговли в данный момент.");
				return;
			}

			const firstTradableTicker = tradableShares[0].ticker;

			// Set details for the modal
			tradeDetails = {
				action,
				quantity,
				displayTicker: displayTicker,
				tradeTicker: firstTradableTicker,
				actionText: action === "buy" ? "покупку" : "продажу"
			};
			isTradeModalOpen = true; // Open the modal
		} catch (e: any) {
			alert(`Произошла ошибка: ${e.message}`);
			console.error("Initiate trade error:", e);
		} finally {
			isTradeLoading = false;
		}
	}

	async function confirmTrade() {
		if (!tradeDetails) return;

		isTradeLoading = true; // Show loading on the confirm button
		const token = localStorage.getItem("access_token");

		try {
			// Place the order using details from the modal state
			const orderResponse = await fetch("http://176.124.212.149:8000/api/invest/sandbox/orders", {
				method: "POST",
				headers: {
					"Content-Type": "application/json",
					Authorization: `Bearer ${token}`
				},
				body: JSON.stringify({
					ticker: tradeDetails.tradeTicker,
					quantity: tradeDetails.quantity,
					direction: tradeDetails.action
				})
			});

			const orderResult = await orderResponse.json();
			if (!orderResponse.ok) {
				throw new Error(orderResult.detail || "Не удалось совершить сделку.");
			}

			alert(
				`Ордер на ${tradeDetails.actionText} ${tradeDetails.quantity} лот(ов) ${tradeDetails.displayTicker} успешно размещен!`
			);
			fetchBalance(); // Refresh balance
		} catch (e: any) {
			alert(`Произошла ошибка: ${e.message}`);
			console.error("Trade confirmation error:", e);
		} finally {
			isTradeLoading = false;
			isTradeModalOpen = false; // Close modal
			tradeDetails = null; // Reset details
		}
	}

	async function fetchBalance() {
		if (!browser) return;
		const token = localStorage.getItem("access_token");
		if (!token) return; // Should not happen if called correctly

		isBalanceLoading = true;
		try {
			const response = await fetch("http://176.124.212.149:8000/api/invest/sandbox/balance", {
				headers: {
					Authorization: `Bearer ${token}`
				}
			});

			if (response.ok) {
				const data = await response.json();
				userBalance = { balance: data.balance, currency: data.currency };
			} else {
				console.error("Failed to fetch balance:", await response.text());
				userBalance = null; // Reset on failure
			}
		} catch (e) {
			console.error("Error fetching balance:", e);
			userBalance = null;
		} finally {
			isBalanceLoading = false;
		}
	}

	async function fetchImportantNews() {
		if (!browser) return;
		const token = localStorage.getItem("access_token");
		if (!token) {
			isImportantNewsLoading = false;
			return;
		}
		isImportantNewsLoading = true;
		try {
			const response = await fetch("http://176.124.212.149:8000/api/news/latest-24h", {
				headers: { Authorization: `Bearer ${token}` }
			});

			if (response.ok) {
				const data = await response.json();
				// Shuffle array (Fisher-Yates)
				for (let i = data.length - 1; i > 0; i--) {
					const j = Math.floor(Math.random() * (i + 1));
					[data[i], data[j]] = [data[j], data[i]];
				}

				const randomSample = data.slice(0, 7);

				importantNews = randomSample.map((item: any) => ({
					id: item.id,
					title: item.title,
					ticker: item.tickers?.split(",")[0].trim() || null,
					tags: item.tags ? item.tags.split(",").map((t: string) => t.trim()) : []
				}));
			} else {
				console.error("Failed to fetch important news");
				importantNews = [];
			}
		} catch (e) {
			console.error("Error fetching important news:", e);
			importantNews = [];
		} finally {
			isImportantNewsLoading = false;
		}
	}
</script>

